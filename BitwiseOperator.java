
public class BitwiseOperator {
	public static void main(String[] args)   
	{   
	int x = 9, y = 8;   
	// bitwise and   
	// 1001 & 1000 = 1000 = 8  
	System.out.println("x & y = " + (x & y));  
	System.out.println("x | y = " + (x | y)); 
	int z = 2;   
	// bitwise compliment  
	// ~0010= 1101 = -3  
	System.out.println("~x = " + (~z)); 
	}  
}
